#Program to find the real and imaginary parts of an array
import numpy as np
x=np.sqrt([2+5j])
y=np.sqrt([3+6j])
print("original array of x is: ",x)
print("Original array of y is: ",y)
print("Real part of array: ")
print(x.real)
print(y.real)
print("Imaginary part of array is : ")
print(x.imag)
print(y.imag)


